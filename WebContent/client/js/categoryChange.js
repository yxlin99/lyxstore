function categoryChange(category)
{
	switch (category) {
	case "earings":
		return "耳环";
		break;
	case "rings":
		return "戒指";
		break;
	case "necklace":
		return "项链";
		break;
	case "bracelet":
		return "手链";
		break;
	case "hairband":
		return "发夹";
		break;
	default:
		break;
	}
	
}